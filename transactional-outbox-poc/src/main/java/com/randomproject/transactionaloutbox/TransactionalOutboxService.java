package com.randomproject.transactionaloutbox;

import jakarta.annotation.PostConstruct;
import org.springframework.jdbc.core.JdbcTemplate;
import org.springframework.scheduling.annotation.Scheduled;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import java.sql.ResultSet;
import java.sql.Timestamp;
import java.time.Instant;
import java.util.List;
import java.util.UUID;

@Service
public class TransactionalOutboxService {
    private static final int DEFAULT_CONSUMER_FAILURE_THRESHOLD = 3;

    private final JdbcTemplate jdbcTemplate;
    private int publishFailuresRemaining;
    private boolean consumerPaused;
    private int consumerFailureThreshold = DEFAULT_CONSUMER_FAILURE_THRESHOLD;

    public TransactionalOutboxService(JdbcTemplate jdbcTemplate) {
        this.jdbcTemplate = jdbcTemplate;
    }

    @PostConstruct
    void initializeSchema() {
        jdbcTemplate.execute("""
                create table if not exists orders (
                    id varchar(64) primary key,
                    customer_id varchar(120) not null,
                    sku varchar(120) not null,
                    quantity int not null,
                    status varchar(40) not null,
                    created_at timestamp not null,
                    updated_at timestamp not null
                )
                """);
        jdbcTemplate.execute("""
                create table if not exists outbox_events (
                    id varchar(64) primary key,
                    aggregate_id varchar(64) not null,
                    event_type varchar(80) not null,
                    payload clob not null,
                    status varchar(40) not null,
                    attempts int not null,
                    last_error varchar(500),
                    created_at timestamp not null,
                    published_at timestamp
                )
                """);
        jdbcTemplate.execute("""
                create table if not exists broker_messages (
                    id bigint generated by default as identity primary key,
                    outbox_event_id varchar(64) not null,
                    event_id varchar(64) not null,
                    event_type varchar(80) not null,
                    payload clob not null,
                    status varchar(40) not null,
                    attempts int not null,
                    last_error varchar(500),
                    created_at timestamp not null,
                    consumed_at timestamp
                )
                """);
        jdbcTemplate.execute("""
                create table if not exists inbox_entries (
                    event_id varchar(64) primary key,
                    broker_message_id bigint not null,
                    handler varchar(120) not null,
                    processed_at timestamp not null
                )
                """);
        jdbcTemplate.execute("""
                create table if not exists audit_events (
                    id bigint generated by default as identity primary key,
                    event_type varchar(80) not null,
                    detail varchar(1000) not null,
                    created_at timestamp not null
                )
                """);
    }

    @Transactional
    public synchronized OrderView createOrder(CreateOrderRequest request) {
        String orderId = "ord_" + shortId();
        String outboxEventId = "evt_" + shortId();
        Instant now = Instant.now();
        String payload = """
                {"orderId":"%s","customerId":"%s","sku":"%s","quantity":%d,"poisonEvent":%s}
                """.formatted(orderId, clean(request.customerId()), clean(request.sku()), request.quantity(), request.poisonEvent()).trim();

        jdbcTemplate.update("""
                        insert into orders (id, customer_id, sku, quantity, status, created_at, updated_at)
                        values (?, ?, ?, ?, ?, ?, ?)
                        """,
                orderId,
                request.customerId(),
                request.sku(),
                request.quantity(),
                OrderStatus.CREATED.name(),
                timestamp(now),
                timestamp(now)
        );
        jdbcTemplate.update("""
                        insert into outbox_events
                        (id, aggregate_id, event_type, payload, status, attempts, created_at)
                        values (?, ?, ?, ?, ?, ?, ?)
                        """,
                outboxEventId,
                orderId,
                "OrderCreated",
                payload,
                OutboxStatus.PENDING.name(),
                0,
                timestamp(now)
        );
        addAudit("TX_COMMIT", "Created " + orderId + " and outbox event " + outboxEventId + " in the same DB transaction.");
        return order(orderId);
    }

    @Scheduled(fixedDelay = 1200)
    public synchronized void relayPendingOutboxEvents() {
        List<OutboxEventView> pending = jdbcTemplate.query("""
                        select * from outbox_events
                        where status = ?
                        order by created_at
                        limit 5
                        """,
                this::mapOutboxEvent,
                OutboxStatus.PENDING.name()
        );
        for (OutboxEventView event : pending) {
            publishOutboxEvent(event);
        }
    }

    @Scheduled(fixedDelay = 1500)
    public synchronized void consumeReadyBrokerMessages() {
        if (consumerPaused) {
            return;
        }
        List<BrokerMessageView> readyMessages = jdbcTemplate.query("""
                        select * from broker_messages
                        where status = ?
                        order by id
                        limit 5
                        """,
                this::mapBrokerMessage,
                BrokerMessageStatus.READY.name()
        );
        for (BrokerMessageView message : readyMessages) {
            consumeBrokerMessage(message);
        }
    }

    public synchronized DemoSnapshot snapshot() {
        return new DemoSnapshot(
                stats(),
                orders(),
                outboxEvents(),
                brokerMessages(),
                inboxEntries(),
                auditEvents()
        );
    }

    public synchronized List<OrderView> orders() {
        return jdbcTemplate.query("select * from orders order by created_at desc", this::mapOrder);
    }

    public synchronized List<OutboxEventView> outboxEvents() {
        return jdbcTemplate.query("select * from outbox_events order by created_at desc", this::mapOutboxEvent);
    }

    public synchronized List<BrokerMessageView> brokerMessages() {
        return jdbcTemplate.query("select * from broker_messages order by id desc", this::mapBrokerMessage);
    }

    public synchronized List<InboxEntryView> inboxEntries() {
        return jdbcTemplate.query("select * from inbox_entries order by processed_at desc", this::mapInboxEntry);
    }

    public synchronized List<AuditEventView> auditEvents() {
        return jdbcTemplate.query("select * from audit_events order by id desc limit 30", this::mapAuditEvent);
    }

    public synchronized OutboxStats stats() {
        return new OutboxStats(
                count("select count(*) from orders"),
                count("select count(*) from outbox_events where status = 'PENDING'"),
                count("select count(*) from outbox_events where status = 'PUBLISHED'"),
                count("select count(*) from broker_messages where status = 'READY'"),
                count("select count(*) from broker_messages where status = 'CONSUMED'"),
                count("select count(*) from broker_messages where status = 'DUPLICATE'"),
                count("select count(*) from broker_messages where status = 'DLQ'"),
                count("select count(*) from inbox_entries"),
                publishFailuresRemaining > 0,
                consumerPaused,
                consumerFailureThreshold
        );
    }

    public synchronized void armPublishFailures(int failures) {
        publishFailuresRemaining = Math.max(0, failures);
        addAudit("FAILURE_ARMED", "Next " + publishFailuresRemaining + " relay publish attempt(s) will fail before hitting the broker.");
    }

    public synchronized void setConsumerPaused(boolean paused) {
        consumerPaused = paused;
        addAudit(paused ? "CONSUMER_PAUSED" : "CONSUMER_RESUMED", "Consumer paused = " + consumerPaused + ".");
    }

    public synchronized void setConsumerFailureThreshold(int threshold) {
        consumerFailureThreshold = Math.max(1, threshold);
        addAudit("CONSUMER_THRESHOLD", "Poison messages move to DLQ after " + consumerFailureThreshold + " failed attempt(s).");
    }

    public synchronized void publishDuplicate(String outboxEventId) {
        OutboxEventView event = findOutboxEvent(outboxEventId);
        jdbcTemplate.update("""
                        insert into broker_messages
                        (outbox_event_id, event_id, event_type, payload, status, attempts, created_at)
                        values (?, ?, ?, ?, ?, ?, ?)
                        """,
                event.id(),
                event.id(),
                event.eventType(),
                event.payload(),
                BrokerMessageStatus.READY.name(),
                0,
                timestamp(Instant.now())
        );
        addAudit("DUPLICATE_PUBLISH", "Republished event " + event.id() + " with the same event id so the inbox can reject it.");
    }

    public synchronized void drainOnce() {
        relayPendingOutboxEvents();
        consumeReadyBrokerMessages();
    }

    public synchronized void reset() {
        jdbcTemplate.update("delete from inbox_entries");
        jdbcTemplate.update("delete from broker_messages");
        jdbcTemplate.update("delete from outbox_events");
        jdbcTemplate.update("delete from orders");
        jdbcTemplate.update("delete from audit_events");
        publishFailuresRemaining = 0;
        consumerPaused = false;
        consumerFailureThreshold = DEFAULT_CONSUMER_FAILURE_THRESHOLD;
        addAudit("RESET", "Cleared demo state.");
    }

    private void publishOutboxEvent(OutboxEventView event) {
        Instant now = Instant.now();
        if (publishFailuresRemaining > 0) {
            publishFailuresRemaining -= 1;
            jdbcTemplate.update("""
                            update outbox_events
                            set attempts = attempts + 1, last_error = ?
                            where id = ?
                            """,
                    "Simulated broker outage",
                    event.id()
            );
            addAudit("RELAY_FAILED", "Relay could not publish " + event.id() + "; outbox row remains PENDING.");
            return;
        }

        jdbcTemplate.update("""
                        insert into broker_messages
                        (outbox_event_id, event_id, event_type, payload, status, attempts, created_at)
                        values (?, ?, ?, ?, ?, ?, ?)
                        """,
                event.id(),
                event.id(),
                event.eventType(),
                event.payload(),
                BrokerMessageStatus.READY.name(),
                0,
                timestamp(now)
        );
        jdbcTemplate.update("""
                        update outbox_events
                        set status = ?, attempts = attempts + 1, last_error = null, published_at = ?
                        where id = ?
                        """,
                OutboxStatus.PUBLISHED.name(),
                timestamp(now),
                event.id()
        );
        addAudit("RELAY_PUBLISHED", "Relay published " + event.id() + " to the broker table.");
    }

    private void consumeBrokerMessage(BrokerMessageView message) {
        if (count("select count(*) from inbox_entries where event_id = ?", message.eventId()) > 0) {
            jdbcTemplate.update("""
                            update broker_messages
                            set status = ?, consumed_at = ?, last_error = ?
                            where id = ?
                            """,
                    BrokerMessageStatus.DUPLICATE.name(),
                    timestamp(Instant.now()),
                    "Inbox already processed event id " + message.eventId(),
                    message.id()
            );
            addAudit("DUPLICATE_SKIPPED", "Consumer skipped broker message " + message.id() + " because event " + message.eventId() + " is already in the inbox.");
            return;
        }

        int nextAttempt = message.attempts() + 1;
        if (message.payload().contains("\"poisonEvent\":true")) {
            if (nextAttempt >= consumerFailureThreshold) {
                String orderId = extractJsonValue(message.payload(), "orderId");
                jdbcTemplate.update("""
                                update broker_messages
                                set status = ?, attempts = ?, last_error = ?, consumed_at = ?
                                where id = ?
                                """,
                        BrokerMessageStatus.DLQ.name(),
                        nextAttempt,
                        "Poison payload rejected by handler",
                        timestamp(Instant.now()),
                        message.id()
                );
                jdbcTemplate.update("update orders set status = ?, updated_at = ? where id = ?",
                        OrderStatus.POISONED.name(),
                        timestamp(Instant.now()),
                        orderId
                );
                addAudit("MESSAGE_DLQ", "Moved broker message " + message.id() + " to DLQ after " + nextAttempt + " failed attempt(s).");
                return;
            }
            jdbcTemplate.update("""
                            update broker_messages
                            set attempts = ?, last_error = ?
                            where id = ?
                            """,
                    nextAttempt,
                    "Poison payload rejected by handler",
                    message.id()
            );
            addAudit("CONSUMER_RETRY", "Handler failed message " + message.id() + "; it remains READY for retry.");
            return;
        }

        String orderId = extractJsonValue(message.payload(), "orderId");
        Instant now = Instant.now();
        jdbcTemplate.update("""
                        insert into inbox_entries (event_id, broker_message_id, handler, processed_at)
                        values (?, ?, ?, ?)
                        """,
                message.eventId(),
                message.id(),
                "order-email-handler",
                timestamp(now)
        );
        jdbcTemplate.update("""
                        update broker_messages
                        set status = ?, attempts = ?, consumed_at = ?, last_error = null
                        where id = ?
                        """,
                BrokerMessageStatus.CONSUMED.name(),
                nextAttempt,
                timestamp(now),
                message.id()
        );
        jdbcTemplate.update("update orders set status = ?, updated_at = ? where id = ?",
                OrderStatus.EMAIL_SENT.name(),
                timestamp(now),
                orderId
        );
        addAudit("CONSUMER_PROCESSED", "Consumer processed event " + message.eventId() + " and recorded it in the inbox.");
    }

    private OutboxEventView findOutboxEvent(String outboxEventId) {
        List<OutboxEventView> events = jdbcTemplate.query(
                "select * from outbox_events where id = ?",
                this::mapOutboxEvent,
                outboxEventId
        );
        if (events.isEmpty()) {
            throw new IllegalArgumentException("Unknown outbox event: " + outboxEventId);
        }
        return events.get(0);
    }

    private OrderView order(String orderId) {
        return jdbcTemplate.queryForObject("select * from orders where id = ?", this::mapOrder, orderId);
    }

    private void addAudit(String eventType, String detail) {
        jdbcTemplate.update("insert into audit_events (event_type, detail, created_at) values (?, ?, ?)",
                eventType,
                detail,
                timestamp(Instant.now())
        );
    }

    private int count(String sql, Object... args) {
        Integer value = jdbcTemplate.queryForObject(sql, Integer.class, args);
        return value == null ? 0 : value;
    }

    private OrderView mapOrder(ResultSet rs, int rowNum) throws java.sql.SQLException {
        return new OrderView(
                rs.getString("id"),
                rs.getString("customer_id"),
                rs.getString("sku"),
                rs.getInt("quantity"),
                OrderStatus.valueOf(rs.getString("status")),
                instant(rs.getTimestamp("created_at")),
                instant(rs.getTimestamp("updated_at"))
        );
    }

    private OutboxEventView mapOutboxEvent(ResultSet rs, int rowNum) throws java.sql.SQLException {
        return new OutboxEventView(
                rs.getString("id"),
                rs.getString("aggregate_id"),
                rs.getString("event_type"),
                rs.getString("payload"),
                OutboxStatus.valueOf(rs.getString("status")),
                rs.getInt("attempts"),
                rs.getString("last_error"),
                instant(rs.getTimestamp("created_at")),
                instant(rs.getTimestamp("published_at"))
        );
    }

    private BrokerMessageView mapBrokerMessage(ResultSet rs, int rowNum) throws java.sql.SQLException {
        return new BrokerMessageView(
                rs.getLong("id"),
                rs.getString("outbox_event_id"),
                rs.getString("event_id"),
                rs.getString("event_type"),
                rs.getString("payload"),
                BrokerMessageStatus.valueOf(rs.getString("status")),
                rs.getInt("attempts"),
                rs.getString("last_error"),
                instant(rs.getTimestamp("created_at")),
                instant(rs.getTimestamp("consumed_at"))
        );
    }

    private InboxEntryView mapInboxEntry(ResultSet rs, int rowNum) throws java.sql.SQLException {
        return new InboxEntryView(
                rs.getString("event_id"),
                rs.getLong("broker_message_id"),
                rs.getString("handler"),
                instant(rs.getTimestamp("processed_at"))
        );
    }

    private AuditEventView mapAuditEvent(ResultSet rs, int rowNum) throws java.sql.SQLException {
        return new AuditEventView(
                rs.getLong("id"),
                rs.getString("event_type"),
                rs.getString("detail"),
                instant(rs.getTimestamp("created_at"))
        );
    }

    private static Timestamp timestamp(Instant instant) {
        return Timestamp.from(instant);
    }

    private static Instant instant(Timestamp timestamp) {
        return timestamp == null ? null : timestamp.toInstant();
    }

    private static String shortId() {
        return UUID.randomUUID().toString().substring(0, 8);
    }

    private static String clean(String value) {
        return value.replace("\"", "");
    }

    private static String extractJsonValue(String payload, String key) {
        String needle = "\"" + key + "\":\"";
        int start = payload.indexOf(needle);
        if (start < 0) {
            return "";
        }
        int valueStart = start + needle.length();
        int valueEnd = payload.indexOf('"', valueStart);
        return valueEnd < 0 ? "" : payload.substring(valueStart, valueEnd);
    }
}
